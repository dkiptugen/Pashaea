                        <p>While the just concluded party primaries left some aspirants celebrating, it has left others
                            especially incumbent politicians with a bitter taste in their mouths.</p>

                        <p>See Also:Individuals who made party primaries a rough ride for aspirants</p>
                        <p>In Kiambu and Kirinyaga, for example, Governor William Kabogo and Governor Joseph Ndathi were
                            the first casualties - they were voted out. As much as the ousted politicians would like
                            Kenyans to believe that the party primaries were rigged, the inalienable fact is that
                            Kenyans have expressed their wishes through the ballot box. Broadly speaking, Kenyan voters
                            seemed to have been fed up with some politiciansâ€™ bad leadership.</p>

                        <p>Needless to say, in Kenya today there are myriad problems that confront mwananchi every day.
                            For example, corruption, unemployment, insecurity, road carnage, human rights violation,
                            ethnic and tribal prejudice, land grabbing among other issues have remained a thorn in the
                            flesh to Wanjiku.</p>

                        Political dinosaurs who have outlived their usefulness in the current political arena should exit and give room to visionary leaders. The political opportunists and self-seekers whose primary objective is to enrich themselves and their henchmen must be shown the door come August 8 General Election


